export function ProductIcon({ icon, className = "", size = 24 }: ProductIconProps) {
  const IconComponent = iconMap[icon];
  
  // Convert size to Tailwind classes
  const getSizeClasses = (size: number) => {
    switch (size) {
      case 16: return "w-4 h-4";
      case 20: return "w-5 h-5";
      case 24: return "w-6 h-6";
      case 32: return "w-8 h-8";
      case 40: return "w-10 h-10";
      case 48: return "w-12 h-12";
      default: return "w-6 h-6";
    }
  };
  
  const sizeClasses = getSizeClasses(size);
  
  if (!IconComponent) {
    // Fallback to a generic icon if the specific icon isn't found
    const GenericIcon = iconMap.brain;
    return <GenericIcon className={`${sizeClasses} ${className}`} />;
  }
  
  return <IconComponent className={`${sizeClasses} ${className}`} />;
}

// Export the icon map for use in other components
export { iconMap };

// Helper function to check if an icon exists
export function hasIcon(iconName: string): boolean {
  return iconName in iconMap;
}

// Helper function to get all available icon names
export function getAvailableIcons(): string[] {
  return Object.keys(iconMap);
}
